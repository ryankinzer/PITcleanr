    path = stringr::str_replace(path,
                                'LLR[0-9]',
                                'LLR'),
    path = stringr::str_replace(path,
                                'Salmon_Other[0-9]',
                                'Salmon_Other'),
    path = stringr::str_replace(path,
                                'MFSR_Other[0-9]',
                                'MFSR_Other'),
    path = stringr::str_replace(path,
                                'Snake_Other[0-9]',
                                'Snake_Other'),
    path = stringr::str_replace(path,
                                'Below_GRA_Other[0-9]',
                                'Below_GRA_Other'),
    path = stringr::str_replace(path,
                                'Below_GRA_Other[0-9]',
                                'Below_GRA_Other'),
    path = stringr::str_replace(path,
                                'Below_GRA_Other[0-9]',
                                'Below_GRA_Other'),
    path = ifelse(
      SiteID %in% c('IR1', 'IR4'),
      stringr::str_replace(path,
                           'IR$',
                           SiteID),
      path)) %>%
  mutate(path = ifelse(
    stringr::str_sub(path, start = -nchar(SiteID)) != SiteID,
    paste(path, SiteID, sep = '.'),
    path
  ))

