  bind_cols(network_descrip %>%
              as.data.frame()) %>%
  mutate_at(vars(matches('^Step')),
            funs(as.character)) %>%
  mutate(SiteID = factor(SiteID,
                         levels = unique(
                           site_df_init$SiteID
                         ))) %>%
  arrange(SiteID)


return(site_df)
